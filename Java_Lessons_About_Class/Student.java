package Java_Lessons_About_Class;

public class Student {
    private String gradeYear;
    private String studentId;
    private String studentName;
    private String studentExtracurricular;

    // Getters
    public String getGradeYear() {
        return gradeYear;
    }

    public String getStudentId() {
        return studentId;
    }

    public String getStudentName() {
        return studentName;
    }

    public String getStudentExtracurricular() {
        return studentExtracurricular;
    }

    // Setters
    public void setGradeYear(String gradeYear) {
        this.gradeYear = gradeYear;
    }

    public void setStudentId(String studentId) {
        this.studentId = studentId;
    }

    public void setStudentName(String studentName) {
        this.studentName = studentName;
    }

    public void setStudentExtracurricular(String studentExtracurricular) {
        this.studentExtracurricular = studentExtracurricular;
    }
}
